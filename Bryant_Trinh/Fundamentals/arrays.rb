a= [1,5,7,8,6,5,10,58,4]

a.at(5)#gets value at 5th index
a.delete(7)# removes any value that is a 7
"some".reverse #reverses the string "some"
"some".length #gives the length of the string
a.sort #sorts the array in numerical order
a.slice(0,3)#get the part of the array in the given range
a.shuffle #gives a shuffled order of the array
a.values_at(1,3).join('and') #gives the values at 1st and 3rd index join together with an 'and'
a.insert(4,10) #inserts a 10 into the 4th index

a.include?(6) #checks to see if 6 is in the array
a.last # displays last index in the array
a.max #displays the max value in the array
a.min #displays the min value in the array

